console.log(`1 ${(10 >= 5)}`);  // maior que

console.log(`2 ${(10 >= 5)} ${(5 >= 5)}`); // maior ou igual que

console.log(`3 ${(5 < 10)}`);  // menor que

console.log(`4 ${(5 < 10)} ${(5 <= 5)}`);  // maior ou igual que

console.log(`5 ${(5 === 5)}`);  // igual que

console.log(`6 ${(10 !== 5)}`);  // diferente que
